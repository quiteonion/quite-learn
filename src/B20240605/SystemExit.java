package B20240605;

public class SystemExit {
    public static void main(String[] args) {
        /**
         * exit
         * 停止虚拟机
         * 如果是0 ： 正常停止
         * 如果不是0 ： 异常停止
         */
        System.out.println("123456");
        System.exit(0);
        System.out.println(678900);
    }
}
