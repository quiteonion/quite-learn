package B20240603;

public class MathRound {
    public static void main(String[] args) {
        /**
         *round
         * Math 类 掌管四舍五入的神
         */
        for (double i = 0; i < 1; ) {
            System.out.println("放入" + i + "输出" + Math.round(i));
            i += 0.1;
        }
    }
}
